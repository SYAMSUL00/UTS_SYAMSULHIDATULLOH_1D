n_A = int(input("masukan nilai A :"))
n_B = int(input("masukan nilai B :"))

hasilperjumlahan = n_A + n_B
hasilpengurangan = n_A - n_B
hasilperkalian = n_A * n_B


print("Hasil Perjumlahan")
print(hasilperjumlahan)
print("Hasil Pengurangan")
print(hasilpengurangan)
print("Hasil Perkalian")
print(hasilperkalian)